    <p>Hornbills are unique birds. They get their name from the horn-like projection called a casque on top of their beak. They are larger than other forest birds. Hornbills are flashy with their over-sized beaks, bright skin around their eyes and long eyelashes. Most have a brilliantly coloured pouch of loose skin at their throat in which they carry fruits, their favourite food.</p>

<p>Hornbills are the 'farmers of the forest' as they disperse the seeds of many tropical trees and keep the forest alive. Unfortunately, most hornbill species are threatened by habitat loss and hunting.</p>

<p>This citizen science initiative allows you to participate and help. We are looking for records and  images of the nine species found in  India - Great Hornbill, Rufous-necked Hornbill, Wreathed Hornbill, Narcondam Hornbill, Malabar Pied Hornbill, Oriental Pied Hornbill, White-throated Brown Hornbill, Malabar Grey Hornbill and the Indian Grey Hornbill.</p>

