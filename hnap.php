  	<h3>Saving hornbill homes</h3>
    <p><em>Adopt hornbill nests and help protect threatened hornbill nests and habitats in Arunachal Pradesh</em></p>
    <p>The Pakke Wildlife Sanctuary & Tiger Reserve in western Arunachal Pradesh is a haven for four species of hornbills in a region where hunting for various body parts, and habitat loss seriously threaten these birds. Since 2003, Pakke is well-protected due to the efforts of the Arunachal Pradesh Forest Department. NCF has a long-term hornbill nest and monitoring program in Pakke since 2003 where 30-40 hornbill nests were monitored each year in the park. However, the last two decades has seen rapid loss of the lowland forests outside Pakke, which is crucial nesting habitat for hornbills. This loss has mainly occurred in adjoining Assam, but the Papum Reserve Forest (1000 km<sup>2</sup>), which adjoins Pakke, which has, over the years, seen forest loss along with areas in adjoining Assam. Northern parts of Papum still harbor dense forests.</p>
    <p>There was a need to ensure conservation of hornbill habitats outside and expand the protection and monitoring effort to areas outside the park. The best way to do this was to seek the participation of the Nyishi community who had banned hunting of hornbills. The Ghora-Aabhe (a local NGO formed by Nyishi village headmen for promoting conservation around Pakke) had been formed in 2006 with the support of the park management to help protection efforts.</p>
    <p>This led us in 2011 to expand our program of hornbill nest monitoring and protection and we sought the participation of the Nyishi community around Pakke who had banned hornbill hunting.</p>
    <p>A three-way partnership between the Ghora-Aabhe Society, the Arunachal Pradesh Forest Department and the Nature Conservation Foundation led to the creation of the Hornbill Nest Adoption Program and an agreement has been made to protect hornbill nests. Nine villages along the southern boundary of Pakke are currently participating with 12 nest protectors, who find, monitor and protect nests of four hornbill species. Some of them are village headmen and former hunters.</p>
    <p>The idea was to engage and bring together two sets of distant people united by the common desire to conserve; local and urban.</p>
    <p>The local community contributes by searching for, monitoring and protecting nests in the forests around their villages, while the urban community contributes by supporting the program financially and assisting as volunteers and coming as visitors.</p>
    <p>A unique aspect of this program is that financial support is coming from urban citizens in different parts of India. They act as foster parents by adopting hornbill nests, while local people look after the nests to ensure they are not disturbed and that nest trees are not cut down. Concerned individuals adopt a hornbill nest at a cost (Rs. 6,000). They can also choose to adopt more nests or give a larger amount. They, in turn, receive information about the status of the nests and what happened to the hornbills that nested in a given year.</p>
    <p>About 100 people have adopted nests, with most continuing their support every year. The Greater Vancouver Zoo, Canada, has also supported the program in 2013.</p>
    <p>In 2013, 26 nests were found in the Papum RF. Of these 12 were active (five Great, six Oriental Pied, and one Wreathed) and 11 successful, resulting in a nesting success rate of 91%.</p>
    <p>The money is used to sustain the program and pay the salaries of nest protectors, and provide some funds for community development and welfare.</p>
